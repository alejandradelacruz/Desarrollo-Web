//DIFERENCIA ENTRE METODO Y FUNCION 
const numero1 = 20;
const numero2 = "20";

//Para convertir un variable a String 
console.log(parseInt(numero2)); //parseInt es una funcion
console.log(numero1.toString()); //.toString es un metodo 

//Nota: Los que tienen un punto seguido de la funcion son metodos 
//      Los que tienen el nombre de la funcion seguido del parentesis son funciones